package com.callor.var;

public class Var_02 {
	public static void main(String[] args) {
		
		
		int num1 = 200;
	
		
		int num2 = 0;
		num2 = 50;
		/* 변수에 값을 저장하거나 읽으려면 반드시 이전
		 * 변수가 최소한 선언되어 있어야 한다.
		 *  */
		
		
		System.out.println(num1 + num2);

		
		int num5 = 0;
		 num5 = 1000;
		 System.out.println(num5);
		 num5 = -4;
		
		System.out.println(num5);
		/*
		 * 변수의 특징으로는 같은 이름이 반복될 경우 그 안에 저장해 있는 것을 없에고 다시 할당한다.
		 */
		}

}
